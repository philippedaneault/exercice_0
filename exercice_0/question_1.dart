main() {
  var largeur = 20;
  var hauteur = 5 * 9.3;
  var r = largeur * hauteur;
  print(r);
}

// 1-Tous les programmes Dart commencent par la fonction "main". Les fonctions telles que "main" sont 
// habituellement suivies par des parenthèses afin de donner des arguments à ces fonctions. Dans le cas
// présent, "main" n'a pas besoin d'argument précis. La première ligne se termine par une accolade ouvrante, 
// qui indique où commence le code. L'accolade ouvrante est toujours située sur la même ligne que la fonction
// de départ "main".
// 2-Les espaces au début de la ligne de code permettent d'améliorer la lisibilité. Ici, on définit la variable 
// "largeur" en lui attribuant une valeur numérique de 20. Le point-virgule dénote la fin de la ligne de code.
// 3-On définit la variable "hauteur" en lui donnant une valeur numérique correspondant à "5 * 9.3".
// 4-On définit la variable "r" par la multiplication de la variable "largeur" par la variable "hauteur".
// 5-La fonction "print" permet au programme d'afficher la variable "r" dans la console de l'éditeur Dart.
// La valeur de "r" correspond ici à 930.
// 6-L'accolande fermante indique la fin de l'écriture du code. Cette accolade se retrouve dans la même
// colonne que le début de la fonction "main"; c'est une convention d'écriture.